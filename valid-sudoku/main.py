# https://leetcode.com/problems/valid-sudoku/

from typing import List


class Solution:
    def isValidSudoku(self, board: List[List[str]]) -> bool:
        rows = [set() for _ in range(9)]
        cols = [set() for _ in range(9)]
        squares = [set() for _ in range(9)]

        for i in range(9):
            for j in range(9):
                val = board[i][j]
                if val == ".":
                    continue

                if val in rows[i] or val in cols[j] or val in squares[getSquare(i, j)]:
                    return False

                rows[i].add(val)
                cols[j].add(val)
                squares[getSquare(i, j)].add(val)
        return True


def getSquare(i, j):
    return i // 3 * 3 + j // 3


